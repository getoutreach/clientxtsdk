/**
 * Configuration value entered by user
 *
 * @export
 * @class ConfigurationValue
 */
export class ConfigurationValue {
  /**
   *
   * Configuration item key
   *
   * @type {string}
   * @memberof ConfigurationValue
   */
  public key: string;
  /**
   *
   * Configuration item value user provided
   *
   * @type {string}
   * @memberof ConfigurationValue
   */
  public value: string;
}
